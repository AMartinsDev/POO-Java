package Lab6;

public class MusicTitle {

    private String title;
    private String artist;

    public MusicTitle() {
        this.title = "Sem dono";
        this.artist = "Sem dono";
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getArtist() {
        return artist;
    }

    public void setArtist(String artist) {
        this.artist = artist;
    }
}
